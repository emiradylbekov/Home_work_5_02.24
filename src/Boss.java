public class Boss {
    private int health;
    private int damage;
    private String Protection;


    public int getHealth() {
        return health;
    }
    public void setHealth() {
        this.health = 600;
    }

    public int getDamage() {
        return damage;
    }
    public void setDamage() {
        this.damage = 60;
    }


    public String getProtection() {
        return Protection;
    }


    public void setProtection() {
        this.Protection = "Heroes attack";
    }

}
